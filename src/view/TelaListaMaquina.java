    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
      int linhaSelecionada = tblMaquinas.getSelectedRow();

    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma máquina!");
        return;
    }

    int id = (int) tblMaquinas.getValueAt(linhaSelecionada, 0);

    int opcao = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir a máquina ID " + id + "?",
            "Confirmação",
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (opcao == javax.swing.JOptionPane.YES_OPTION) {

        MaquinaDAO dao = new MaquinaDAO();

        if (dao.excluir(id)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Máquina excluída com sucesso!");
            carregarTabela();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Erro ao excluir máquina!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
    }
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      
        int linhaSelecionada = tblMaquinas.getSelectedRow();

if (linhaSelecionada == -1) {
    javax.swing.JOptionPane.showMessageDialog(this,
            "Selecione uma máquina para editar!");
    return;
}

Maquina m = new Maquina();
m.setId((int) tblMaquinas.getValueAt(linhaSelecionada, 0));
m.setNome((String) tblMaquinas.getValueAt(linhaSelecionada, 1));
m.setEspecificacoes((String) tblMaquinas.getValueAt(linhaSelecionada, 2));
m.setStatus((String) tblMaquinas.getValueAt(linhaSelecionada, 3));
m.setLaboratorioId((int) tblMaquinas.getValueAt(linhaSelecionada, 4));

// Abre a tela de máquina em modo edição
new TelaMaquina(m, this).setVisible(true);

    }//GEN-LAST:event_btnEditarActionPerformed

